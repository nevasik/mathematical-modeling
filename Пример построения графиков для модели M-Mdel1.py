# –ì–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞—Ç—å –ø–æ –∑–∞–∫–æ–Ω—É –ü—É–∞—Å—Å–æ–Ω–∞, —É–∫–∞–∑—ã–≤–∞—è —Å–≤–æ—é ùúÜ (–∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —á–µ–ª–æ–≤–µ–∫ –≤ —á–∞—Å).–ò—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Ä–µ–∞–ª—å–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ, –µ—Å–ª–∏ –æ–Ω–∏ –µ—Å—Ç—å.

# –ü–æ—Å—Ç—Ä–æ–µ–Ω–∏–µ –≥—Ä–∞—Ñ–∏–∫–∞
# –ò—Å–ø–æ–ª—å–∑—É–µ–º plt.bar, —á—Ç–æ–±—ã –≤–∏–∑—É–∞–ª–∏–∑–∏—Ä–æ–≤–∞—Ç—å —á–∏—Å–ª–æ –ø—Ä–∏—à–µ–¥—à–∏—Ö —Å—Ç—É–¥–µ–Ω—Ç–æ–≤ –∑–∞ –∫–∞–∂–¥—ã–π —á–∞—Å.
# –ü–æ –æ—Å–∏ X —Ä–∞–∑–º–µ—â–∞–µ–º –º–µ—Ç–∫–∏ (08:00, 09:00, ‚Ä¶, 13:00).
# –û–±—Ä–∞—Ç–∏—Ç–µ –≤–Ω–∏–º–∞–Ω–∏–µ, —á—Ç–æ –≤ —Ç–∞–∫–æ–π –ø–æ—Å—Ç–∞–Ω–æ–≤–∫–µ –º—ã —Ñ–∞–∫—Ç–∏—á–µ—Å–∫–∏ —Å—á–∏—Ç–∞–µ–º, —á—Ç–æ —Å 08:00 –¥–æ 09:00 –ø—Ä–∏—à–ª–æ arrivals_per_hour[0] —á–µ–ª–æ–≤–µ–∫, —Å 09:00 –¥–æ 10:00 ‚Äì arrivals_per_hour[1] –∏ —Ç.–¥.