import random
import matplotlib.pyplot as plt

# –î–æ–ø—É—Å—Ç–∏–º, —É –Ω–∞—Å —É—á–µ–±–Ω—ã–π –¥–µ–Ω—å —Å 08:00 –¥–æ 14:00
# –í–æ–∑—å–º—ë–º –∫–∞–∂–¥—É—é —Ü–µ–ª—É—é ¬´–≥—Ä–∞–Ω–∏—Ü—É¬ª —á–∞—Å–∞:
times_hours = ["08:00", "09:00", "10:00", "11:00", "12:00", "13:00", "14:00"]

# –ü—Ä–µ–¥–ø–æ–ª–æ–∂–∏–º, —á—Ç–æ –∑–∞ –∫–∞–∂–¥—ã–π —á–∞—Å –º—ã –±—É–¥–µ–º –≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞—Ç—å
# —Å–ª—É—á–∞–π–Ω–æ–µ —á–∏—Å–ª–æ ¬´–ø—Ä–∏–±—ã–≤—à–∏—Ö¬ª —Å—Ç—É–¥–µ–Ω—Ç–æ–≤ (–ª–∏–±–æ –ø–æ –ü—É–∞—Å—Å–æ–Ω—É, –ª–∏–±–æ –ø—Ä–æ—Å—Ç–æ randint).
# –ó–¥–µ—Å—å –¥–ª—è —É–ø—Ä–æ—â–µ–Ω–∏—è –∏—Å–ø–æ–ª—å–∑—É–µ–º randint (2, 20),
# –Ω–æ –º–æ–∂–Ω–æ –∏ –ø—É–∞—Å—Å–æ–Ω–æ–≤—Å–∫—É—é –≥–µ–Ω–µ—Ä–∞—Ü–∏—é –¥–µ–ª–∞—Ç—å, –∫–∞–∫ –≤ –ø—Ä–µ–¥—ã–¥—É—â–∏—Ö –ø—Ä–∏–º–µ—Ä–∞—Ö.

arrivals_per_hour = []
for i in range(len(times_hours) - 1):
    # –î–ª—è —á–∞—Å–∞ [times_hours[i], times_hours[i+1]] —Å–≥–µ–Ω–µ—Ä–∏—Ä—É–µ–º –æ–¥–Ω–æ —á–∏—Å–ª–æ
    arrivals = random.randint(2, 20)
    arrivals_per_hour.append(arrivals)

# –¢–µ–ø–µ—Ä—å —É –Ω–∞—Å times_hours = ["08:00", "09:00", "10:00", "11:00", "12:00", "13:00", "14:00"]
# –∏ arrivals_per_hour = [–Ω–∞–ø—Ä–∏–º–µ—Ä, 14, 6, 10, 17, 9, 12]

# –ß—Ç–æ–±—ã –ø–æ—Å—Ç—Ä–æ–∏—Ç—å –≥—Ä–∞—Ñ–∏–∫, —É–¥–æ–±–Ω–µ–µ –∏–º–µ—Ç—å —Ç–æ—á–∫–∏ –ø–æ —Ü–µ–Ω—Ç—Ä–∞–º –∏–Ω—Ç–µ—Ä–≤–∞–ª–æ–≤.
# –¢–æ –µ—Å—Ç—å, –Ω–∞–ø—Ä–∏–º–µ—Ä, ¬´08:00‚Äì09:00¬ª ‚Äì —ç—Ç–æ —á–∞—Å, –º–æ–∂–Ω–æ –ø–æ–¥–ø–∏—Å–∞—Ç—å –æ—Å—å –∫–∞–∫ ¬´08:00¬ª,
# –∞ –∑–Ω–∞—á–µ–Ω–∏–µ arrivals –≤–∑—è—Ç—å –¥–ª—è –∏–Ω—Ç–µ—Ä–≤–∞–ª–∞ (08:00..09:00).

# –ü–æ—Å—Ç—Ä–æ–∏–º –ø—Ä–æ—Å—Ç–æ–π –≥—Ä–∞—Ñ–∏–∫ (–∏–ª–∏ —Å—Ç–æ–ª–±—á–∞—Ç—É—é –¥–∏–∞–≥—Ä–∞–º–º—É ‚Äì bar chart).
x_labels = times_hours[:-1]  # –ø–æ–¥–ø–∏—Å–∏ –ø–æ –Ω–∞—á–∞–ª—É –∫–∞–∂–¥–æ–≥–æ —á–∞—Å–∞, –∫—Ä–æ–º–µ –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ (14:00 ‚Äì —ç—Ç–æ –≥—Ä–∞–Ω–∏—Ü–∞ –æ–∫–æ–Ω—á–∞–Ω–∏—è)
x_positions = range(len(x_labels))  # [0, 1, 2, 3, 4, 5]

plt.figure(figsize=(8, 5))
plt.bar(x_positions, arrivals_per_hour, width=0.6, color='skyblue', edgecolor='black')

# –ù–∞—Å—Ç—Ä–æ–∏–º –ø–æ–¥–ø–∏—Å–∏ –Ω–∞ –æ—Å–∏ X
plt.xticks(x_positions, x_labels, rotation=0)  # –ø–æ–¥–ø–∏—à–µ–º —Å—Ç–æ–ª–±–∏–∫–∏: 08:00, 09:00, ...

plt.xlabel("–ù–∞—á–∞–ª–æ —á–∞—Å–æ–≤–æ–≥–æ –∏–Ω—Ç–µ—Ä–≤–∞–ª–∞")
plt.ylabel("–ß–∏—Å–ª–æ –ø—Ä–∏—à–µ–¥—à–∏—Ö —Å—Ç—É–¥–µ–Ω—Ç–æ–≤ (rnd)")
plt.title("–ü—Ä–∏–º–µ—Ä —á–∞—Å–æ–≤–æ–π –¥–∏—Å–∫—Ä–µ—Ç–∏–∑–∞—Ü–∏–∏ –ø–æ—Ç–æ–∫–∞ (08:00-14:00)")

plt.grid(axis='y', linestyle='--', alpha=0.7)
plt.tight_layout()
plt.show()

# –ú–æ–∂–Ω–æ –≤—ã–≤–µ—Å—Ç–∏ —Å—É–º–º—É
print("–°—É–º–º–∞—Ä–Ω–æ–µ —á–∏—Å–ª–æ —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö —Å—Ç—É–¥–µ–Ω—Ç–æ–≤:", sum(arrivals_per_hour))



